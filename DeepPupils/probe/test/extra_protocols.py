def smb_rebuild(payloads):
    """Build TCP session from (is_server, data) payloads list."""
    from scapy.all import IP, TCP, Raw, wrpcap, conf as _conf
    _conf.verb = 0
    pkts = []
    s_ip, d_ip = "10.10.10.1", "10.10.10.2"
    seq, ack = 5000, 6000
    c_seq, s_seq = seq+1, ack+1
    pkts.append(IP(src=s_ip,dst=d_ip)/TCP(sport=49152,dport=445,flags='S',seq=seq))
    pkts.append(IP(src=d_ip,dst=s_ip)/TCP(sport=445,dport=49152,flags='SA',seq=ack,ack=seq+1))
    pkts.append(IP(src=s_ip,dst=d_ip)/TCP(sport=49152,dport=445,flags='A',seq=c_seq,ack=s_seq))
    for is_server, data in payloads:
        if not is_server:
            pkts.append(IP(src=s_ip,dst=d_ip)/TCP(sport=49152,dport=445,flags='PA',seq=c_seq,ack=s_seq)/Raw(load=data))
            c_seq += len(data)
        else:
            pkts.append(IP(src=d_ip,dst=s_ip)/TCP(sport=445,dport=49152,flags='PA',seq=s_seq,ack=c_seq)/Raw(load=data))
            s_seq += len(data)
    pkts.append(IP(src=s_ip,dst=d_ip)/TCP(sport=49152,dport=445,flags='FA',seq=c_seq,ack=s_seq))
    pkts.append(IP(src=d_ip,dst=s_ip)/TCP(sport=445,dport=49152,flags='FA',seq=s_seq,ack=c_seq+1))
    pkts.append(IP(src=s_ip,dst=d_ip)/TCP(sport=49152,dport=445,flags='A',seq=c_seq+1,ack=s_seq+1))
    return pkts

